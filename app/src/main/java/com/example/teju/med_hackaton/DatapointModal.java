package com.example.teju.med_hackaton;

public class DatapointModal {
    double x, y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public DatapointModal(double x, double y) {

        this.x = x;
        this.y = y;
    }
}
